// การปิดบังคุณสมบัติใน Prototype Chain

// เมื ่อออบเจกต์มีคุณสมบัติที ่มีชื ่อเดียวกับหนึ ่งในคุณสมบัติของโปรโตไทป์ของมัน คุณสมบัติของออบเจกต์จะ ปิดบัง คุณสมบัติ
// ของโปรโตไทป์ ซึ ่งหมายความว่าคุณสมบัติของออบเจกต์เองจะถูกเข้าถึงแทนคุณสมบัติของโปรโตไทป์

// ตัวอย่างของการปิดบังคุณสมบัติ

const animal = {
    type: "Mammal"
};

const dog = Object.create(animal);
dog.type = "Canine";

console.log(dog.type); // Canine
console.log(animal.type); // Mammal